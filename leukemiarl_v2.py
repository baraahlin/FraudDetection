        cv = StratifiedKFold(n_splits=5, shuffle=True, random_state=42)
        y_pred_cv = cross_val_predict(model_pipeline, X_subset, self.y, cv=cv)

        gmean = geometric_mean_score(self.y, y_pred_cv)

        alpha_weight = 0.99
        feature_ratio = len(selected_cols) / self.total_features
        fitness = (alpha_weight * gmean) + ((1.0 - alpha_weight) * (1.0 - feature_ratio))

