def evaluate_new_models(feature_indices, model, model_name):
    start_time = time.time()
    X_train_sub, X_test_sub = X_train[:, feature_indices], X_test[:, feature_indices]

    model_pipeline = Pipeline([
        ('scaler', StandardScaler()),
        ('smote', SMOTE(random_state=42)),
        ('classifier', model)
    ])

    model_pipeline.fit(X_train_sub, y_train)
    y_pred = model_pipeline.predict(X_test_sub)

    exec_time = time.time() - start_time
    acc = accuracy_score(y_test, y_pred)
    gmean = geometric_mean_score(y_test, y_pred)

    print(f"\n[{model_name} with {len(feature_indices)} Golden Features]")
    print(f"Accuracy  : {acc:.4f} | G-mean: {gmean:.4f} | Exec Time: {exec_time:.4f}s")

