def hellinger_distance(feature_col, y):
    """Calculates Hellinger Distance to keep minority-class predictive features."""
    class_0 = feature_col[y == 0]
    class_1 = feature_col[y == 1]

    bins = np.histogram_bin_edges(feature_col, bins='auto')
    p, _ = np.histogram(class_0, bins=bins, density=True)
    q, _ = np.histogram(class_1, bins=bins, density=True)

    p = p + 1e-10
    q = q + 1e-10

    h_dist = (1.0 / np.sqrt(2.0)) * np.sqrt(np.sum((np.sqrt(p) - np.sqrt(q)) ** 2))
    return h_dist

def filter_features(X, y, top_k=10):
    """Discards noisy features while keeping those with high Hellinger scores."""
    scores = [hellinger_distance(X[:, i], y) for i in range(X.shape[1])]
    top_feature_indices = np.argsort(scores)[-top_k:]
    return X[:, top_feature_indices], top_feature_indices, scores

