class FeatureGroupingEnv(gym.Env):
    def __init__(self, X, y, num_tasks=3):
        super(FeatureGroupingEnv, self).__init__()
        self.X = X
        self.y = y
        self.num_features = X.shape[1]
        self.num_tasks = num_tasks
        self.current_feature_idx = 0

        self.observation_space = spaces.Box(low=0, high=1, shape=(4,), dtype=np.float32)
        self.action_space = spaces.Discrete(self.num_tasks)

        self.feature_assignments = np.full(self.num_features, -1)
        self.selection_history = np.zeros(self.num_features)

        self.lambda_1, self.lambda_2, self.lambda_3, self.lambda_4 = 1.0, 0.5, 0.1, 2.0

    def _get_observation(self):
        f_idx = self.current_feature_idx
        np.random.seed(f_idx)
        f_importance = np.random.uniform(0.5, 1.0)

        f_redundancy = 0.0
        if self.current_feature_idx > 0:
            assigned_features = self.X[:, :self.current_feature_idx]
            current_f_data = self.X[:, f_idx]
            correlations = [np.abs(np.corrcoef(current_f_data, assigned_features[:, j])[0, 1])
                            for j in range(self.current_feature_idx)]
            f_redundancy = np.mean(correlations) if correlations else 0.0

        f_task_similarity = len(np.unique(self.feature_assignments[self.feature_assignments != -1])) / self.num_tasks
        f_history = self.selection_history[f_idx]

        return np.array([f_importance, f_redundancy, f_task_similarity, f_history], dtype=np.float32)

    def step(self, action):
        self.feature_assignments[self.current_feature_idx] = action
        self.selection_history[self.current_feature_idx] += 0.1

        self.current_feature_idx += 1
        done = self.current_feature_idx >= self.num_features

        reward = 0.0
        if done:
            reward = self._calculate_reward()

        observation = np.zeros(4, dtype=np.float32) if done else self._get_observation()
        return observation, reward, done, False, {}

    def _calculate_reward(self):
        total_gmean = 0.0
        total_redundancy = 0.0
        active_tasks = 0

        for t in range(self.num_tasks):
            task_indices = np.where(self.feature_assignments == t)[0]

            if len(task_indices) == 0:
                continue

            X_subset = self.X[:, task_indices]

            # --- THE SAFETY NET ---
            # If a task has fewer than 3 features, give it a flat baseline score
            # so the AI isn't terrified to start filling it up.
            if len(task_indices) < 3:
                total_gmean += 0.5 # Baseline "okay" score
                total_redundancy += 0.0
            else:
                knn = KNeighborsClassifier(n_neighbors=3)
                knn.fit(X_subset, self.y)
                y_pred = knn.predict(X_subset)
                total_gmean += geometric_mean_score(self.y, y_pred)

                corr_matrix = np.corrcoef(X_subset, rowvar=False)
                redundancy = np.nansum(np.triu(corr_matrix, k=1)) / (len(task_indices) + 1e-5)
                total_redundancy += redundancy

            active_tasks += 1

        if active_tasks == 0:
            return -20.0

        avg_gmean = total_gmean / active_tasks
        avg_redundancy = total_redundancy / active_tasks

        empty_task_penalty = (self.num_tasks - active_tasks) * 10.0
        c_complexity = (self.num_features / active_tasks) / self.num_features

        reward = (self.lambda_4 * avg_gmean) - (self.lambda_2 * avg_redundancy) - (self.lambda_3 * c_complexity) - empty_task_penalty
        return reward

    def reset(self, seed=None, options=None):
        super().reset(seed=seed)
        self.current_feature_idx = 0

        # --- THE WARM START FIX ---
        # Instead of starting with all features unassigned (-1),
        # we randomly distribute them evenly across the 3 tasks to begin with!
        self.feature_assignments = np.random.randint(0, self.num_tasks, size=self.num_features)

        # Reset history
        self.selection_history = np.zeros(self.num_features)

        return self._get_observation(), {}

