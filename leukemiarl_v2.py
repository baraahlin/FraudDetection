import numpy as np
import copy
from sklearn.neighbors import KNeighborsClassifier
from imblearn.metrics import geometric_mean_score

class MFGWfs:
    def __init__(self, X, y, rl_task_assignments, num_wolves=5, max_iter=10, rmp=0.3):
        self.X = X
        self.y = y
        self.task_assignments = rl_task_assignments
        self.num_tasks = len(np.unique(rl_task_assignments))
        self.num_wolves = num_wolves
        self.max_iter = max_iter
        self.rmp = rmp # Random Mating Probability (for IGT)

        self.tasks_features = {}
        self.populations = {}

        # Track Top 3 Wolves for each task
        self.alpha_pos, self.alpha_score = {}, {}
        self.beta_pos, self.beta_score = {}, {}
        self.delta_pos, self.delta_score = {}, {}

        for t in range(self.num_tasks):
            self.tasks_features[t] = np.where(self.task_assignments == t)[0]
            num_features = len(self.tasks_features[t])

            # Start wolves with random binary feature subsets
            self.populations[t] = np.random.randint(2, size=(self.num_wolves, num_features)).astype(float)

            # --- THE FIX IS HERE ---
            # Initialize ALL leader positions to zero arrays
            self.alpha_pos[t] = np.zeros(num_features)
            self.beta_pos[t] = np.zeros(num_features)
            self.delta_pos[t] = np.zeros(num_features)

            # Initialize leader scores to negative infinity
            self.alpha_score[t] = -np.inf
            self.beta_score[t] = -np.inf
            self.delta_score[t] = -np.inf

    def fitness_function(self, wolf_binary, task_id):
        """Cost-Sensitive G-mean Evaluation"""
        active_indices = np.where(wolf_binary >= 0.5)[0]
        if len(active_indices) == 0: return 0.0

        selected_cols = self.tasks_features[task_id][active_indices]
        if len(selected_cols) == 0: return 0.0

        X_subset = self.X[:, selected_cols]
        knn = KNeighborsClassifier(n_neighbors=3)
        knn.fit(X_subset, self.y)
        y_pred = knn.predict(X_subset)
        return geometric_mean_score(self.y, y_pred)

    def optimize(self):
        """The Main GWO Hunt with Implicit Genetic Transfer"""
        print("\n--- STARTING MFGWfs OPTIMIZATION ---")

        for iteration in range(self.max_iter):
            a = 2.0 - iteration * (2.0 / self.max_iter)

            for t in range(self.num_tasks):
                num_features = len(self.tasks_features[t])
                if num_features == 0: continue

                # 1. EVALUATE FITNESS & FIND LEADERS
                for i in range(self.num_wolves):
                    score = self.fitness_function(self.populations[t][i], t)

                    if score > self.alpha_score[t]:
                        self.delta_score[t], self.delta_pos[t] = self.beta_score[t], copy.deepcopy(self.beta_pos[t])
                        self.beta_score[t], self.beta_pos[t] = self.alpha_score[t], copy.deepcopy(self.alpha_pos[t])
                        self.alpha_score[t], self.alpha_pos[t] = score, copy.deepcopy(self.populations[t][i])
                    elif score > self.beta_score[t]:
                        self.delta_score[t], self.delta_pos[t] = self.beta_score[t], copy.deepcopy(self.beta_pos[t])
                        self.beta_score[t], self.beta_pos[t] = score, copy.deepcopy(self.populations[t][i])
                    elif score > self.delta_score[t]:
                        self.delta_score[t], self.delta_pos[t] = score, copy.deepcopy(self.populations[t][i])

            # 2. UPDATE WOLF POSITIONS (The Hunt)
            for t in range(self.num_tasks):
                num_features = len(self.tasks_features[t])
                if num_features == 0: continue

                for i in range(self.num_wolves):
                    for j in range(num_features):
                        r1, r2 = np.random.rand(), np.random.rand()
                        A1, C1 = 2 * a * r1 - a, 2 * r2
                        D_alpha = abs(C1 * self.alpha_pos[t][j] - self.populations[t][i][j])
                        X1 = self.alpha_pos[t][j] - A1 * D_alpha

                        r1, r2 = np.random.rand(), np.random.rand()
                        A2, C2 = 2 * a * r1 - a, 2 * r2
                        D_beta = abs(C2 * self.beta_pos[t][j] - self.populations[t][i][j])
                        X2 = self.beta_pos[t][j] - A2 * D_beta

                        r1, r2 = np.random.rand(), np.random.rand()
                        A3, C3 = 2 * a * r1 - a, 2 * r2
                        D_delta = abs(C3 * self.delta_pos[t][j] - self.populations[t][i][j])
                        X3 = self.delta_pos[t][j] - A3 * D_delta

                        new_pos = (X1 + X2 + X3) / 3.0

                        # --- IMPLICIT GENETIC TRANSFER (IGT) ---
                        if np.random.rand() < self.rmp and self.num_tasks > 1:
                            other_task = np.random.choice([x for x in range(self.num_tasks) if x != t])
                            # Make sure the other task isn't empty before trying to steal its genes
                            if len(self.tasks_features[other_task]) > 0:
                                random_gene = np.random.choice(self.alpha_pos[other_task])
                                new_pos = (new_pos + random_gene) / 2.0

                        sigmoid = 1.0 / (1.0 + np.exp(-10 * (new_pos - 0.5)))
                        self.populations[t][i][j] = 1 if sigmoid > np.random.rand() else 0

            # Find the best valid task to print progress
            best_t = 0
            for t_idx in range(self.num_tasks):
                if len(self.tasks_features[t_idx]) > 0:
                    best_t = t_idx
                    break
            print(f"Iteration {iteration+1}/{self.max_iter} Completed. Best Task {best_t} G-mean: {self.alpha_score[best_t]:.4f}")

        print("\n--- OPTIMIZATION FINISHED ---")

